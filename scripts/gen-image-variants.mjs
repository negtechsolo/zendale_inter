/**
 * Responsive image variants.
 *
 * Reads every raster image in public/images and writes smaller WebP copies
 * beside it (name-400.webp, name-800.webp, name-1200.webp), then regenerates
 * src/lib/imageMeta.ts with the intrinsic size of each source image.
 *
 * SmartImage uses that map to emit width/height (no layout shift, correct
 * aspect ratio) and a srcset (the browser downloads the size it actually
 * needs instead of the full-resolution original).
 *
 * Run it whenever images are added or replaced:
 *   npm run images
 *
 * Requires `sharp` (a devDependency). If sharp is unavailable the script
 * exits quietly and the build continues with original-size images only.
 */
import { readdir, stat, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import path from "node:path";

const ROOT = path.resolve(process.cwd());
const IMAGE_DIR = path.join(ROOT, "public", "images");
const META_FILE = path.join(ROOT, "src", "lib", "imageMeta.ts");
const WIDTHS = [96, 400, 800, 1200];
const VARIANT_RE = /-(96|400|800|1200)\.webp$/;

let sharp;
try {
  ({ default: sharp } = await import("sharp"));
} catch {
  console.warn("[images] sharp is not installed — skipping variant generation.");
  process.exit(0);
}

async function walk(dir) {
  const out = [];
  for (const entry of await readdir(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) out.push(...(await walk(full)));
    else if (/\.(webp|png|jpe?g)$/i.test(entry.name) && !VARIANT_RE.test(entry.name)) out.push(full);
  }
  return out;
}

const files = (await walk(IMAGE_DIR)).sort();
const meta = {};
let written = 0;

for (const file of files) {
  const rel = path.relative(IMAGE_DIR, file).split(path.sep).join("/");
  const image = sharp(file);
  const { width, height } = await image.metadata();
  const variants = [];

  for (const w of WIDTHS) {
    if (w >= width) continue;
    const out = file.replace(/\.(webp|png|jpe?g)$/i, `-${w}.webp`);
    variants.push(w);
    if (existsSync(out)) {
      const [a, b] = await Promise.all([stat(file), stat(out)]);
      if (b.mtimeMs >= a.mtimeMs) continue; // already current
    }
    await sharp(file).resize({ width: w, withoutEnlargement: true })
      .webp({ quality: 74, effort: 5 }).toFile(out);
    written += 1;
  }

  meta[rel] = { w: width, h: height, v: variants };
}

const body = `/**
 * AUTO-GENERATED by scripts/gen-image-variants.mjs — do not edit by hand.
 * Run \`npm run images\` after adding or replacing anything in public/images.
 */
export interface ImageMeta {
  /** intrinsic width of the source file */
  w: number;
  /** intrinsic height of the source file */
  h: number;
  /** widths available as pre-generated variants */
  v: number[];
}

export const imageMeta: Record<string, ImageMeta> = ${JSON.stringify(meta, null, 2)};

/** Builds a srcset string for a file in /public/images, or "" if none exists. */
export function srcSetFor(file: string): string {
  const entry = imageMeta[file];
  if (!entry || entry.v.length === 0) return "";
  const stem = file.replace(/\\.(webp|png|jpe?g)$/i, "");
  const ext = file.slice(stem.length);
  const parts = entry.v.map((w) => \`/images/\${stem}-\${w}.webp \${w}w\`);
  parts.push(\`/images/\${stem}\${ext} \${entry.w}w\`);
  return parts.join(", ");
}
`;

await writeFile(META_FILE, body, "utf8");
console.log(`[images] ${files.length} sources, ${written} variants written, meta -> src/lib/imageMeta.ts`);
